CREATE DATABASE IF NOT EXISTS trivial;

CREATE TABLE IF NOT EXISTS trivial.usuario (
	id INT AUTO_INCREMENT PRIMARY KEY,
	nombre VARCHAR(100),
	login VARCHAR(60),
	pass VARCHAR(200),
	correo VARCHAR(100)	
	) engine = InnoDB;
	
CREATE TABLE IF NOT EXISTS trivial.jugador (
	id INT PRIMARY KEY,
	ganadas INT,
	FOREIGN KEY (id) REFERENCES trivial.usuario(id) 
	ON DELETE CASCADE
	) engine = InnoDB;
	
CREATE TABLE IF NOT EXISTS trivial.admins (
	id INT PRIMARY KEY,
	ult_login DATE,
	FOREIGN KEY (id) REFERENCES trivial.usuario(id)
	) engine = InnoDB;
	
CREATE TABLE IF NOT EXISTS trivial.partida (
	id INT AUTO_INCREMENT PRIMARY KEY,
	nombre VARCHAR(60)
	) engine = InnoDB;	

CREATE TABLE IF NOT EXISTS trivial.tema (
	id INT AUTO_INCREMENT PRIMARY KEY,
	tema VARCHAR(50),
	color VARCHAR(30)
	) engine = InnoDB;
	
CREATE TABLE IF NOT EXISTS trivial.categoria (
	id INT AUTO_INCREMENT PRIMARY KEY,
	categoria VARCHAR(50),
	id_tema INT,
	FOREIGN KEY (id_tema) REFERENCES trivial.tema(id)
	) engine = InnoDB;	
	
CREATE TABLE IF NOT EXISTS trivial.respuesta (
	id INT AUTO_INCREMENT PRIMARY KEY,
	resp VARCHAR(600),
	id_cat INT,
	FOREIGN KEY (id_cat) REFERENCES trivial.categoria(id)
	) engine = InnoDB;

CREATE TABLE IF NOT EXISTS trivial.dificultad (
	id INT AUTO_INCREMENT PRIMARY KEY,
	dificultad VARCHAR(50)
	) engine = InnoDB;
	
CREATE TABLE IF NOT EXISTS trivial.pregunta (
	id INT AUTO_INCREMENT PRIMARY KEY,
	preg VARCHAR(1000),
	id_resp INT,
	id_cat INT,
	id_dif INT,
	FOREIGN KEY (id_resp) REFERENCES trivial.respuesta(id),
	FOREIGN KEY (id_cat) REFERENCES trivial.categoria(id),
	FOREIGN KEY (id_dif) REFERENCES trivial.dificultad(id)
	) engine = InnoDB;
	
CREATE TABLE IF NOT EXISTS trivial.Preguntajugada (
	id INT AUTO_INCREMENT PRIMARY KEY,
	id_jug INT,
	id_par INT,
	id_preg INT,
	correcta BOOLEAN,
	FOREIGN KEY (id_jug) REFERENCES trivial.jugador(id),
	FOREIGN KEY (id_par) REFERENCES trivial.partida(id),
	FOREIGN KEY (id_preg) REFERENCES trivial.pregunta(id)
	) engine = InnoDB;
	
